// if-else ladder

let marks = 76;

if (marks >= 90) {
  console.log("A Grade");
} else if (marks >= 70 && marks < 90) {
  console.log("B Grade");
} else if (marks >= 50 && marks < 70) {
  console.log("C Grade");
} else if (marks >= 30 && marks < 50) {
  console.log("D Grade");
} else {
  console.log("Fail");
}
